class Solution {
public:
    Node* cloneTree(Node* root) {
        if (!root) return nullptr;
        Node* cur = new Node(root->val);
        for (auto subNode: root->children) {
            cur->children.PB(cloneTree(subNode));
        }
        return cur;
    }
};